int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
    cout.tie(nullptr);

    int n, m;
    cin >> n >> m;
    vector<int> a(n), b(m);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    for (int i = 0; i < m; i++) {
        cin >> b[i];
    }

    for (auto elem : b) {
        auto it = find(a.begin(), a.end(), elem);
        if (it != a.end()) {
            a.erase(it);
        }
    }

    for (auto elem : a) {
        cout << elem << " ";
    }

    return 0;
}